public class dozenOpstappelen
{

	public static void main(String[] args)
	{
		int doosTeller = 0; 
		int doosAantal = 25; 

		for(doosTeller = 0; doosTeller < doosAantal; doosTeller = doosTeller + 1)
		{
			System.out.println("zet doos "+doosTeller+" op het pallet.");
		}
	}
}
